def adjust_string_in_file(file_path, part_to_adjust, addition):
    try:
        with open(file_path, 'r') as file:
            lines = file.readlines()

        with open(file_path, 'w') as file:
            for line in lines:
                if part_to_adjust in line:
                    adjusted_line = line.replace(part_to_adjust, addition + part_to_adjust)
                    file.write(adjusted_line)
                else:
                    file.write(line)
                    
        print("Adjustment completed successfully!")
        
    except FileNotFoundError:
        print("File not found.")

